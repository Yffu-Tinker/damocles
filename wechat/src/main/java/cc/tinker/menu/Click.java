package cc.tinker.menu;

public class Click extends Button implements Subable {

	private String type = Button.CLICK;
	private String key;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

}
